    <script>
        function updateClock(timezone) {
            const now = new Date();
            const options = {
                timeZone: timezone,
                hour12: false,
                hour: '2-digit',
                minute: '2-digit',
                second: '2-digit'
            };
            const timeString = now.toLocaleTimeString('en-US', options);
            document.getElementById('clock').textContent = timeString;
        }

        function updateTimezone() {
            const selectedTimezone = document.getElementById('timezone').value;
            updateClock(selectedTimezone);
        }

        // Memanggil updateClock setiap 1 detik (1000 milidetik)
        setInterval(updateTimezone, 1000);

        // Pertama kali panggil untuk menampilkan waktu awal
        updateTimezone();
    </script>
